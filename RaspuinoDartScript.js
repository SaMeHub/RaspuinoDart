    function speak(text, callback) {
      var u = new SpeechSynthesisUtterance();
      // u.text = '<?xml version="1.0"?>\r\n<speak version="1.0" xmlns="http://www.w3.org/2001/10/synthesis" xml:lang="en-US">' + text + '</speak>';
      u.text = text;
      u.lang = 'en-US';
      u.voice = window.speechSynthesis.getVoices().filter(function(voice) { return voice.name == 'Samantha'; })[0];
      u.onend = function () {
        if (callback) {
          callback();
        }
      };
      u.onerror = function (e) {
        if (callback) {
          callback(e);
        }
      };
      window.speechSynthesis.speak(u);
    }

    function sortSelect(x) {
      var my_options = x.find('option');
      var selected = x.val();
      my_options.sort(function(a,b) {
          if (a.text > b.text) return 1;
          if (a.text < b.text) return -1;
          return 0
      })
      x.empty().append( my_options );
      x.val(selected);
    }

